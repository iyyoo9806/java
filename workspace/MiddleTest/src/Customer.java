
public class Customer {
	String productName;
	double realPrice;
	String name;
	String contact;
	String address;

	Customer(String productName, double realPrice, String name, String contact, String address) {
		this.productName = productName;
		this.realPrice = realPrice;
		this.name = name;
		this.contact = contact;
		this.address = address;
	}
}
